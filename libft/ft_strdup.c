#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	char	*str;
	int		len;

	i = 0;
	len = 0;
	while (src[len])
		len++;
	str = (char *)malloc(sizeof(char) * (len + 1));
	if (!str)
		return (NULL);
	i = 0;
	while (i < len)
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
